package com.stom.app.reportrecords;

public class NoviPacijentiPoAdresiMjesecno {
	
	
	private Integer novihPacijenata;
	private Integer mjesec;
	private Integer godina;
	private String adresa;
	
	public NoviPacijentiPoAdresiMjesecno() {	
	}

	public Integer getNovihPacijenata() {
		return novihPacijenata;
	}

	public void setNovihPacijenata(Integer novihPacijenata) {
		this.novihPacijenata = novihPacijenata;
	}

	public Integer getMjesec() {
		return mjesec;
	}

	public void setMjesec(Integer mjesec) {
		this.mjesec = mjesec;
	}

	public Integer getGodina() {
		return godina;
	}

	public void setGodina(Integer godina) {
		this.godina = godina;
	}

	public String getAdresa() {
		return adresa;
	}

	public void setAdresa(String adresa) {
		this.adresa = adresa;
	}
}
